#This is the code for swapping of two numbers
num1 = input("Enter the value of first number :")
num2 = input("Enter the value of second number :")

print("The value of number 1 is " + num1)
print("The value of number 2 is " + num2)

#Swapping
temp = num1
num1 = num2
num2 = temp

print("The values after swapping :-")
print("The value of number 1 is " + num1)
print("The value of number 2 is " + num2)